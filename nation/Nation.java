package nation;

public class Nation {
	protected String name;
	protected int ArcherNum;
	protected int InfantryNum;
	protected int CavalrySoldierNum;

	public int getArcherNum() {
		return ArcherNum;
	}

	public int getInfantryNum() {
		return InfantryNum;
	}

	public int getCavalrySoldierNum() {
		return CavalrySoldierNum;
	}
}
